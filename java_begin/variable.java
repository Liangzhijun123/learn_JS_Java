package java_begin;

public class variable {

    public static void main(String[] args) {
        // declare
        int x;

        // assignment
        x = 123;

        // initialization
        int z = 123;

        long m = 12345566677L;

        float u = 3.14f;

        double y = 3.14;

        boolean a = true;

        char symbol = '@';

        String name = "bro";

        System.out.println("integer: "+z);
        System.out.println("long number: "+m);
        System.out.println("float number: " + u);
        System.out.println("double number: " + y);
        System.out.println("boolean: " + a);
        System.out.println("char: " + symbol);
        System.out.println("String: "+name);
    }

}
